package com.diary.fitness_diary.home;

public class SubItem {
    int time;
    String set;
    float weight;


    public String  getSet() {
        return set;
    }

    public void setSet(String set) {
        this.set = set;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }
}
